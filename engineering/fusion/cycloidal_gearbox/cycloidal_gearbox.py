def build_housing(gb):
    """Back plate (the NEMA 17 bolts to it from behind, pilot boss recessed),
    grooved ring wall, necked collar, and the Ø76 front flange with snug M3
    holes on the Ø66 bolt circle. The Zp ring pins are Ø6 x 25 steel dowels:
    dropped in through the full-length wall grooves (their insertion
    channels, open at the front face — the ear disc caps them once mounted)
    and pressed into blind holes in the plate; each groove wraps its pin past
    the equator, so mesh load pushes the pins INTO the wrap. The grooves
    open as hairline slits on the body OD (the Ø66 BCD leaves no room to
    close them) — cosmetic only. One Ø6.5 tangential tunnel through the wall
    lets a driver reach the output hub's pinch bolt, above the pin tops."""
    b = Builder()
    b.add(b.cyl((0, 0, 0), (0, 0, gb.body_top), gb.body_d))
    b.add(b.cyl((0, 0, gb.body_top), (0, 0, gb.flange_z0), gb.collar_d))
    b.add(b.cyl((0, 0, gb.flange_z0), (0, 0, gb.body_l), gb.flange_d))
    b.cut(b.cyl((0, 0, gb.plate_t), (0, 0, gb.ring_z1), gb.ring_id))
    b.cut(b.cyl((0, 0, gb.ring_z1), (0, 0, gb.body_l + 1), gb.collar_id))
    for j in range(gb.zp):
        a = math.radians(gb.ring_clock_deg) + 2 * math.pi * j / gb.zp
        x = gb.pin_circle_r * math.cos(a)
        y = gb.pin_circle_r * math.sin(a)
        b.cut(b.cyl((x, y, gb.plate_t), (x, y, gb.body_l + 1), gb.groove_d))
        b.cut(b.cyl((x, y, gb.ring_pin_z0), (x, y, gb.plate_t + 0.1),
                    gb.ring_hole_d))
    # motor interface: pilot recess from the back, shaft bore, screws from
    # inside into the motor's own threads (heads sunk below the plate top)
    b.cut(b.cyl((0, 0, -1), (0, 0, NEMA_PILOT_H), NEMA_PILOT_D))
    b.cut(b.cyl((0, 0, -1), (0, 0, gb.plate_t + 0.1), NEMA_BORE_D))
    h = NEMA_HOLE_SPACING / 2
    for dx, dy in ((h, h), (h, -h), (-h, h), (-h, -h)):
        b.cut(b.cyl((dx, dy, -1), (dx, dy, gb.plate_t + 0.1), M3_CLEAR_D))
        b.cut(b.cyl((dx, dy, M3_CB_FLOOR), (dx, dy, gb.plate_t + 0.1), M3_CB_D))
    # mount holes: snug on M3 so the screws center the housing on the joint
    b.cut_bolt_circle_z(0, 0, gb.mount_bcd, gb.mount_n, gb.mount_hole_d,
                        gb.flange_z0 - 1, gb.body_l + 1)
    # pinch-bolt access tunnel, along the bolt chord (+Y side, x = pinch_x);
    # rotate the output until its bolt lines up, then drive through here
    b.cut(b.cyl((gb.pinch_x, gb.hub_d / 2 + 1, gb.pinch_z),
                (gb.pinch_x, gb.flange_d / 2 + 2, gb.pinch_z), gb.tunnel_d))
    return b.body


def build_disc(gb):
    """Cycloid disc at the cam-angle-0 pose (center at +e). Profile by
    simulated hobbing: subtract the pin-radius cutter along the pin-center
    trochoid p(t) = R*u(t) - e*u(Zp*t) seen from the disc frame."""
    e, r = gb.ecc, gb.pin_circle_r
    b = Builder()
    b.add(b.cyl((e, 0, gb.disc_a_z0), (e, 0, gb.disc_a_z1), 2 * gb.tip_r))
    n = HOB_STEPS_PER_LOBE * gb.zc
    for k in range(n):
        t = 2 * math.pi * k / n
        x = e + r * math.cos(t) - e * math.cos(gb.zp * t)
        y = r * math.sin(t) - e * math.sin(gb.zp * t)
        b.cut(b.cyl((x, y, gb.disc_a_z0 - 0.1), (x, y, gb.disc_a_z1 + 0.1),
                    gb.cutter_d))
    b.cut(b.cyl((e, 0, gb.disc_a_z0 - 1), (e, 0, gb.disc_a_z1 + 1),
                gb.disc_bore_d))
    b.cut_bolt_circle_z(e, 0, gb.out_pin_bcd, gb.out_pin_n, gb.out_hole_d,
                        gb.disc_a_z0 - 1, gb.disc_a_z1 + 1)
    return b.body


def build_cam(gb):
    """Eccentric input cam: 5 mm D-bore, two journals 180° apart for the
    6802 bearings (they overlap 0.5 mm axially so the body is one solid).
    Identical journals + identical discs = balanced eccentric masses."""
    e = gb.ecc
    b = Builder()
    b.add(b.cyl((e, 0, gb.cam_z0), (e, 0, gb.cam_mid), CAM_JOURNAL_D))
    b.add(b.cyl((-e, 0, gb.cam_mid - 0.5), (-e, 0, gb.cam_z1), CAM_JOURNAL_D))
    bore = b.cyl((0, 0, gb.cam_z0 - 1), (0, 0, gb.cam_z1 + 1), MOTOR_BORE_D)
    flat = b.box(MOTOR_FLAT_X, MOTOR_BORE_D, -MOTOR_BORE_D, MOTOR_BORE_D,
                 gb.cam_z0 - 1, gb.cam_z1 + 1)
    b.tbm.booleanOperation(bore, flat,
                           adsk.fusion.BooleanTypes.DifferenceBooleanType)
    b.cut(bore)
    return b.body


def build_output(gb):
    """Output flange: six blind press-fit holes take the Ø6 x 25 steel dowel
    pins, which reach back through both discs. The hub's blind bore seats the
    8 mm joint shaft (floor = depth stop, above the motor shaft's pocket) and
    is closed onto it by a split pinch: tangential M3 through the hub,
    captured nut slid in through a side window before the bolt goes in. The
    Ø13 boss bears on the inboard 608 inner race, flush with the flange
    face — it replaces the inboard shaft collar (the outboard collar stays
    on the far clevis ear)."""
    b = Builder()
    b.add(b.cyl((0, 0, gb.fp_z0), (0, 0, gb.fp_z1), gb.fp_d))
    b.add(b.cyl((0, 0, gb.fp_z1), (0, 0, gb.hub_z1), gb.hub_d))
    b.add(b.cyl((0, 0, gb.hub_z1), (0, 0, gb.body_l), gb.boss_d))
    b.cut_bolt_circle_z(0, 0, gb.out_pin_bcd, gb.out_pin_n, gb.pin_hole_d,
                        gb.fp_z0 - 1, gb.pin_hole_top)
    b.cut(b.cyl((0, 0, gb.fp_z0 - 1), (0, 0, gb.pocket_top),
                MOTOR_BORE_D + 3.4))            # motor shaft tip spins here
    b.cut(b.cyl((0, 0, gb.bore_floor), (0, 0, gb.body_l + 1), gb.shaft_bore_d))
    # pinch: slit through +X (flexes about the x<2 hinge), bolt along Y
    b.cut(b.box(2.0, gb.hub_d / 2 + 1, -gb.slit_half, gb.slit_half,
                gb.slit_z0, gb.body_l + 0.5))
    px, pz = gb.pinch_x, gb.pinch_z
    b.cut(b.cyl((px, -gb.hub_d / 2 - 1, pz), (px, gb.hub_d / 2 + 1, pz),
                M3_CLEAR_D))
    b.cut(b.cyl((px, 3.5, pz), (px, gb.hub_d / 2 + 1, pz), 7.0))    # head
    # nut window: breaks out the hub side for insertion; the z walls (across
    # the flats) lock rotation, the bolt itself retains the nut
    b.cut(b.box(px - M3_NUT_AF / 2 - 0.1, gb.hub_d / 2 + 1,
                -M3_NUT_T - 3.2, -3.0,
                pz - M3_NUT_AF / 2 - 0.35, pz + M3_NUT_AF / 2 + 0.35))
    return b.body


def disc_b_matrix(gb, x_off):
    """Occurrence transform mapping the disc-A body to the disc-B pose:
    opposite eccentric (cam at 180°) needs the identical disc clocked half a
    lobe: p -> Rz(-pi/Zc)*(p - (e,0,0)) + (-e,0,dz)."""
    e = gb.ecc
    ang = -math.pi / gb.zc
    m = adsk.core.Matrix3D.create()
    m.setToRotation(ang, adsk.core.Vector3D.create(0, 0, 1),
                    adsk.core.Point3D.create(0, 0, 0))
    tx = -e * math.cos(ang) - e + x_off
    ty = -e * math.sin(ang)
    tz = gb.disc_b_z0 - gb.disc_a_z0
    m.translation = adsk.core.Vector3D.create(tx / 10.0, ty / 10.0, tz / 10.0)
    return m


