"""RobotTwin — cycloidal gearbox generator for Autodesk Fusion (v2, face-mounted).

Generates two single-stage cycloidal gearbox cartridges — 15:1 and 20:1 —
driven entirely by engineering/gearboxes/gear_parameters.yaml, and exports
them per part to engineering/gearboxes/15_1/ and 20_1/:

    rt_cyc<N>_housing  — motor plate + grooved ring (takes Zp Ø6 x 25 steel
                         ring dowels) + Ø76 front mounting flange; bolts
                         face-first to the linkage ear disc (6 x M3 into its
                         heat-set inserts on the Ø66 bolt circle), NEMA 17
                         hangs on the back plate
    rt_cyc<N>_disc     — cycloid disc (x2 per gearbox, cam lobes 180° apart)
    rt_cyc<N>_cam      — eccentric input cam on the motor's 5 mm D-shaft
    rt_cyc<N>_output   — output flange: six Ø6 x 25 steel dowel pins pressed
                         into blind holes reach back through the discs; the
                         hub pinch-clamps the 8 mm joint shaft and its Ø13
                         boss bears on the inboard 608 inner race

Architecture (fixed ring, Zp = Zc + 1 pins, ratio = Zc with reversal): the
motor spins the eccentric cam; two 6802 bearings let the discs wobble on it;
disc lobes walk around the stationary ring pins; the disc's slow rotation
passes through the output holes (Ø = pin + 2e + clearance) to the steel
output pins. Two discs 180° apart balance the eccentric mass and share load.

Hardware assumptions (from the yaml `hardware:` section):
  * every pin is the same Ø6 x 25 mm steel dowel — no printed pins anywhere:
      - ring pins: pressed into blind holes in the back plate and wrapped
        past their equator by full-length wall grooves (also the insertion
        channels, open at the front face); mesh load pushes them into the
        wrap. Ø6 pins force the ring circle to R = 28 (yaml) and the pin
        pattern is clocked so the plate press holes clear the NEMA screws.
      - output pins: pressed ~11.7 mm into the flange — the 25 mm length is
        what sets the Ø62 x 38.4 body (the flange plate absorbs every
        millimetre the discs don't use)
  * main shaft: Ø8 in 608-2RS bearings + shaft collars, all owned by the
    linkage clevis; the hub grips it with a split pinch (tangential M3 +
    captured nut, tightened through an access tunnel in the housing wall)
  * mount: Ø66 bolt circle, 6 x M3 into heat-set inserts in the ear disc
    (cut by linkage_geometry.cut_gearbox_mount). Snug Ø3.2 flange holes make
    the screws locate the housing; the body necks to Ø62/Ø59 so the socket
    heads seat and a hex key passes the bolt circle axially.

The disc profile is generated by simulated hobbing: a cylinder of the ring
pin radius (+ print clearance) is boolean-subtracted along the pin-center
trochoid seen from the disc frame, which yields the exact conjugate envelope
to the sampling resolution. Profile math is validated by
python/validate_cycloid.py, which reads the same yaml.

Z axis = gearbox axis; z = 0 is the back plate face the NEMA 17 bolts to;
z = BODY_L is the front flange face that seats on the linkage ear disc, where
the output boss meets the 608 inner race.

Run from Fusion: UTILITIES > ADD-INS > Scripts and Add-Ins > "+" > select this
folder > Run. Direct modeling, no timeline: edit the yaml and re-run. The
hobbing cuts take a minute or two — Fusion is not hung.

Run from a shell (no Fusion) for a dry run of the parameters and the derived
stack:  python cycloidal_gearbox.py
"""

